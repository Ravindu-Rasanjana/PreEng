# Sum of three numbers

print('================================================')
print('Sum of three numbers')
print('================================================')

num1 = int(input('Enter the First Number : '))
num2 = int(input('Enter the Second Number : '))
num3 = int(input('Enter the Third Number : '))

if num1 == num2 == num3:
    sum = (num1 + num2 + num3) * 3
else:
    sum = num1 + num2 + num3
print(sum)

