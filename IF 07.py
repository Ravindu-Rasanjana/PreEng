#Sum of three numbers

num1 = int(input('Enter the First Number : '))
num2 = int(input('Enter the Second Number : '))
num3 = int(input('Enter the Second Number : '))

if num1 == num2 and num2 == num3 and num1 == num3:
    sum = num1 * 3
else:
    sum = num1 + num2 + num3
print(sum)

